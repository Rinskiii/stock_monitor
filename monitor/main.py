"""
monitor.main
============

Точка входа в приложение Stock Monitor.

Назначение:
    Выполняет один цикл мониторинга:

        1. Загружает выбранный watchlist.
        2. Проверяет все акции на наличие консолидации.
        3. Загружает ранее отправленные уведомления.
        4. Находит новые сигналы.
        5. Отправляет системные уведомления.
        6. Сохраняет текущее состояние сигналов.

    Модуль не содержит бизнес-логики поиска консолидации —
    за это отвечает ``scanner.py``.

Использование:
    python -m monitor.main
"""

from __future__ import annotations

from monitor.notifier import notify
from monitor.scanner import scan_watchlist
from monitor.storage import load_alerts, save_alerts
from monitor.watchlist_loader import load_watchlist


def main() -> None:
    """
    Выполняет один цикл мониторинга.

    Returns:
        None
    """

    # ------------------------------------------------------------------
    # Загружаем watchlist
    # ------------------------------------------------------------------
    watchlist = load_watchlist()

    # ------------------------------------------------------------------
    # Ищем акции, находящиеся в консолидации
    # ------------------------------------------------------------------
    current_alerts = set(scan_watchlist(watchlist))

    # ------------------------------------------------------------------
    # Загружаем ранее отправленные сигналы
    # ------------------------------------------------------------------
    previous_alerts = load_alerts()

    # ------------------------------------------------------------------
    # Определяем только новые сигналы
    # ------------------------------------------------------------------
    new_alerts = current_alerts - previous_alerts

    # ------------------------------------------------------------------
    # Отправляем уведомления
    # ------------------------------------------------------------------
    for ticker in sorted(new_alerts):

        notify(
            title="Stock Monitor",
            message=f"{ticker} entered 5m consolidation.",
        )

    # ------------------------------------------------------------------
    # Сохраняем текущее состояние
    # ------------------------------------------------------------------
    save_alerts(current_alerts)


if __name__ == "__main__":
    main()